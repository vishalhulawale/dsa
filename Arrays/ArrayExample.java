package Arrays;

public class ArrayExample {

    public static void main(String[] args) {

        int[] intArr = new int[5];

        intArr[0] = 5;
        intArr[1] = 7;
        intArr[2] = 9;

        for (int i = 0; i < intArr.length; i++) {

            System.out.println(intArr[i]);

        }

    }

}